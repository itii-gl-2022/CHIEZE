package tp7;

class Produit {
	
	private String id; 
	private String code;
	private String libelle;
	
	public String getCode() {
		return  this.code;
	}
	
	public String getId() {
		return  this.id;
	}
	
	public void setId(String n) {
		this.id = n;
		
	}
	
	public void setCode(String n) {
		this.code = n;
		
	}
	
	public String getLibelle() {
		return  this.libelle;
	}
	
	public void setLibelle(String n) {
		this.libelle= n;
		
	}
	

}
